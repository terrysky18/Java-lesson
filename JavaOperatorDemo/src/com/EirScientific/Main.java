package com.EirScientific;

public class Main {

    public static void main(String[] args) {
	// To demonstrate an assignment shortcut
        boolean first_check = true;

        // Ternary operator
        // short for: if-else statment
        boolean second_check = first_check ? true : false;

        if(second_check)
        {
            System.out.println("First check is true");
        }
        else
        {
            System.out.println("First check is false");
        }
    }
}
